# To illustrate the creation of dictionary

phone={'raj':9876432090,'ram':9834500888,'bhim':8976089760,'shah':7689076890}

# Print all items in dictionary
print(phone)

# print particular item based on key
print(phone['shah'])

# Change the value based on key
phone['ram']=9999988888
print(phone)

# Add new item to dictionary
phone['kumar']=9887886880
print(phone)

# Delete an item from dictionary
del phone['kumar']
print(phone)

# Find length of a dictionary
print(len(phone))

